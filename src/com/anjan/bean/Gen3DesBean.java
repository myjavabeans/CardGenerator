package com.anjan.bean;

public class Gen3DesBean {
	
	private String keyName;
	private String keyPin;
	private String deviceType;
	private String inputString;
	private String encDecType;
	
	public String getKeyName() {
		return keyName;
	}
	public void setKeyName(String keyName) {
		this.keyName = keyName;
	}
	public String getKeyPin() {
		return keyPin;
	}
	public void setKeyPin(String keyPin) {
		this.keyPin = keyPin;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getInputString() {
		return inputString;
	}
	public void setInputString(String inputString) {
		this.inputString = inputString;
	}
	public String getEncDecType() {
		return encDecType;
	}
	public void setEncDecType(String encDecType) {
		this.encDecType = encDecType;
	}

}
